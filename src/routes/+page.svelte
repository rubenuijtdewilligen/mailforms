<h1>rbnu-mailforms</h1>
<p>A service to send emails directly from static html forms.</p>
<p>
  This project is currently not open to the public, but I am planning to release a public version in
  June of this year. Currently, it is being used for contact forms on client's websites.
</p>
<p>
  You may self-host it with zero guarantees. The source code is available under the <a
    href="https://www.gnu.org/licenses/gpl-3.0.nl.html">GPL GNUv3 license</a
  >
  on <a href="https://github.com/rubenuijtdewilligen/mailforms">GitHub</a>.
</p>
